	//moveq r10, #0
	//addeq r0, #1
	ldrnesh r4, [r1, #-4]			//dx
	ldrnesh r10, [r1, #(-4 + 2)]	//dy

	cmp r2, #0
	moveq r5, #0x4000
	//moveq r11, #0
	//addeq r0, #1
	subne r1, #(STRIDE >> 1)
	ldrnesh r5, [r1] //, #-(STRIDE >> 1)]
	ldrnesh r11, [r1, #2] //, #(-(STRIDE >> 1) + 2)]
	addne r1, #(STRIDE >> 1)
	cmpne r3, r7, lsr #3
	moveq r6, #0x4000//#0
	//moveq r12, #0
	//addeq r0, #1
	ldrnesh r6, [r1, #(-(STRIDE >> 1) + 8)]
	ldrnesh r12, [r1, #((-(STRIDE >> 1) + 8) + 2)]

	mov r0, #0
	mov r2, #0

	cmp r4, #0x4000
	moveq r4, #0
	moveq r10, #0
	orreq r0, #1
	addeq r2, #1

	cmp r5, #0x4000
