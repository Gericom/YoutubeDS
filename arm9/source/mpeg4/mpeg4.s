	//get x and y
	mov r2, r9, lsr #STRIDE_SHIFT		//y
	sub r3, r9, r2, lsl #STRIDE_SHIFT	//x

	//divide by 8
	mov r2, r2, lsr #3
	mov r3, r3, lsr #3
	//create new offset
