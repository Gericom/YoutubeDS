	//c
	cmp r2, #0
	moveq r6, #1024
	ldrnesh r6, [r9, #(-(STRIDE >> 2))]
	//get differences
	subs r7, r4, r5
	rsbmi r7, r7, #0
	subs r8, r5, r6
	rsbmi r8, r8, #0
	cmp r7, r8
	movlt r4, r6
