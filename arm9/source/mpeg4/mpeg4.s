	moveq r12, #0
	orreq r0, #4
	addeq r2, #1

	cmp r2, #2
	bne 1f

	tst r0, #1
	moveq r5, r4
	moveq r11, r10
	moveq r6, r4
	moveq r12, r10
	beq 1f
	tst r0, #2
	moveq r4, r5
	moveq r10, r11
	moveq r6, r5
	moveq r12, r11
	beq 1f	
	mov r4, r6
	mov r10, r12
	mov r5, r6
	mov r11, r12

	@ cmp r0, #2
	@ bne 1f	
	@ cmp r3, #0
	@ movne r5, r4
	@ movne r11, r10
	@ movne r6, r4
	@ movne r12, r10
	@ bne 1f
	@ cmp r2, #0
	@ movne r4, r5
	@ movne r10, r11
	@ movne r6, r5
	@ movne r12, r11
	@ bne 1f	
	@ mov r4, r6
	@ mov r10, r12
	@ mov r5, r6
	@ mov r11, r12
1:
	pop {r0}
	//calculate the median of the 3 values for dx
	@median = max(min(a,b), min(max(a,b),c));
	//min(a,b)
	mov r9, r4
	cmp r4, r5
	movgt r9, r5
	//max(a,b)
	mov lr, r4
	movlt lr, r5
	//min(max(a,b),c)
	mov r4, lr
	cmp lr, r6
	movgt r4, r6
	//max(min(a,b), min(max(a,b),c))
	cmp r9, r4
	movgt r4, r9
	//r4 is px now
	//calculate the median of the 3 values for dy
	mov r9, r10
	cmp r10, r11
	movgt r9, r11
	mov lr, r10
	movlt lr, r11
	mov r5, lr
	cmp lr, r12
	movgt r5, r12
	cmp r9, r5
	movgt r5, r9
	//r5 is py now
	pop {r9-r12} //get the other arguments back
	push {r1}
	//Let's decode the vector
	bl mpeg4_motion_vector_decode
	//r11 = dx, r12 = dy
	//Store dx and dy in the cache
	pop {r1}
	strh r11, [r1]
	strh r12, [r1, #2]
	strh r11, [r1, #4]
	strh r12, [r1, #(4 + 2)]
	add r1, #(STRIDE >> 1)
	strh r11, [r1]//, #(STRIDE >> 1)]
	strh r12, [r1, #2] //, #((STRIDE >> 1) + 2)]
	strh r11, [r1, #4] //, #((STRIDE >> 1) + 4)]
	strh r12, [r1, #(4 + 2)] //#((STRIDE >> 1) + 4 + 2)]
	sub r1, #(STRIDE >> 1)
	push {r11, r12}
	//We need to copy the block now
	bl mpeg4_blockcopy_16x16
	pop {r11, r12}
	tst r11, #3
	bic r11, #3
	mov r11, r11, asr #1
	addne r11, #1
	tst r12, #3
	bic r12, #3
	mov r12, r12, asr #1
	addne r12, #1
	push {r8, r11, r12}
	mov r8, r8, lsr #1
	bl mpeg4_blockcopy_8x8_UV
	pop {r8, r11, r12}
	push {r8}
	mov r8, r8, lsr #1
	add r8, #(STRIDE >> 1)
	bl mpeg4_blockcopy_8x8_UV
	pop {r8}
	pop {r1-r7}
